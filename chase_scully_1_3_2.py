def add_tip(total, tip_percent): 
    '''This is a multi-line comment  
       Return the total amount including tip
    '''
    tip = tip_percent*total
    return total + tip
def hyp(leg1, leg2):
    return (leg1**2+leg2**2)**.5   
def mean(a, b, c):
    return (a+b+c)/3.0
def perimeter(base, height):
    a = base
    b = height
    return (a+a+b+b)